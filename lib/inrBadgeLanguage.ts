export const DEFAULT_INRBADGE_LANGUAGE = "fr" as const;

export const INRBADGE_LANGUAGE_OPTIONS = [
  { value: "fr", label: "Français" },
  { value: "en", label: "English" },
  { value: "es", label: "Español" },
  { value: "it", label: "Italiano" },
  { value: "de", label: "Deutsch" },
  { value: "nl", label: "Nederlands" },
  { value: "pt", label: "Português" },
] as const;

export type InrBadgeLanguageCode = (typeof INRBADGE_LANGUAGE_OPTIONS)[number]["value"];

const LANGUAGE_VALUES = new Set<string>(INRBADGE_LANGUAGE_OPTIONS.map((option) => option.value));

export function normalizeInrBadgeLanguage(value: unknown): InrBadgeLanguageCode {
  const raw = String(value || "").trim().toLowerCase();
  return LANGUAGE_VALUES.has(raw) ? (raw as InrBadgeLanguageCode) : DEFAULT_INRBADGE_LANGUAGE;
}

export function getInrBadgeLocale(language: unknown) {
  const lang = normalizeInrBadgeLanguage(language);
  if (lang === "en") return "en-GB";
  if (lang === "es") return "es-ES";
  if (lang === "it") return "it-IT";
  if (lang === "de") return "de-DE";
  if (lang === "nl") return "nl-NL";
  if (lang === "pt") return "pt-PT";
  return "fr-FR";
}

